#include "push_swap.h"

void	pb(t_list *l)
{
	int	i;

	i = l->blen;
	l->blen++;
	l->alen--;
	while (i > 0)
	{
		swap(&l->b[i], &l->b[i - 1]);
		i--;
	}
	l->b[0] = l->a[0];
	i = 0;
	while (i < l->alen)
	{
		l->a[i] = l->a[i + 1];
		i++;
	}
	write(1, "pb\n", 3);
}
