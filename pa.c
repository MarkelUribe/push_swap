#include "push_swap.h"

void	pa(t_stack *l)
{
	int	i;

	i = l->alen;
	l->blen -= 1;
	l->alen += 1;
	while (i > 0)
	{
		swap(&l->a[i], &l->a[i - 1]);
		i--;
	}
	l->a[0] = l->b[0];
	i = 0;
	while (i <= l->blen)
	{
		l->b[i] = l->b[i + 1];
		i++;
	}
	write(1, "pa\n", 3);
}
