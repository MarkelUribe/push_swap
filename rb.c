#include "push_swap.h"

void	rb(l_stack *l)
{
	int	i;

	i = 0;
	while (i + 1 < l->blen)
	{
		swap(&l->b[i], &l->b[i + 1]);
		i++;
	}
	write(1, "rb\n", 3);
}