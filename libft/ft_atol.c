#include "libft.h"

long	ft_atol(const char *str)
{
	long	z;
	int		sign;

	z = 0;
	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n'
		|| *str == '\v' || *str == '\r' || *str == '\f')
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (ft_isdigit(*str))
	{
		z *= 10;
		z += *str - '0';
		str++;
	}
	return (z * sign);
}
